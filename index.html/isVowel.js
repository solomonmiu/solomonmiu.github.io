/**
 * 
 * @param {string} str String to check if it is a vowel
 * @return {boolean} Returns true if it is a vowel 
 */
function isVowel(cha) {
    if (cha === 'a' || cha === 'e' || cha === 'i' || cha === 'o' || cha === 'u') {
        return true;
    }
    return false;
}
